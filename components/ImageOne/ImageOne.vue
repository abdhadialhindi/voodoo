<script lang="ts">
import { gsap } from "gsap";
import { e } from "ofetch/dist/error-8a55452d";

let img: HTMLImageElement;

// const box:any = this.$refs.box;

export default {
  mounted() {
    const boxone: any = this.$refs.boxone; // get the box element from the DOM using refs.

    const imgone: any = this.$refs.imgone; // get the image element from the DOM using refs.
    const imageone: any = this.$refs.imageone;

    // create a timeline instance to animate with gsap

    let tl = gsap.timeline();

    // add a mousemove event listener to the box element

    boxone.addEventListener(
      "mousemove",
      (e: { [x: string]: number; offsetX: number; offsetY: number }) => {
        // calculate the mouse position relative to the center of the box element

        let x = (e.clientX - boxone.offsetWidth / 2) / 5;

        let y = (e.clientY - boxone.offsetHeight / 2) / 5;

        // animate the image position with gsap, relative to its original position in the center of the box element

        tl.to(imgone, 0, { x: -x / 5, y: -y, rotate: x / 50 });
        tl.to(imageone, 0, { x: x / 5, y: y, rotate: x / 50 });
      }
    );
    boxone.addEventListener("mouseleave", () => {
      tl.to(imgone, 0, { x: 0, y: 0, rotate: 0 });
      tl.to(imageone, 0, { x: 0, y: 0, rotate: 0 });
    });
    if (window.innerWidth < 768) {
      window.addEventListener('scroll', this.handleScroll)
    }
},
beforeUnmount() {
    window.removeEventListener('scroll', this.handleScroll)
  },
  methods: {
    handleScroll() {
      const scrollTop = window.pageYOffset
      gsap.to('.scroll-img', { y: scrollTop / 30 })
    }
  }
  
};
